public class even
{
    public static void main(String args[])
    {
        int i;
        System.out.println("Even numbers in the range 1 - 20 are:");
        for(i=1;i<=20;i++)
        {
            if(i%2 == 0)
            {
                System.out.print(i+" ");
            }
        }
    }
}
